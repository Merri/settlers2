					<h2>Buildings &amp; contructing</h2>
					<p>The idea in The Settlers II is to expand, create a flourishing economy where needs are met and, if need be, wage war on enemies. To achieve this a transportation network of roads must be constructed and a balanced amount of various buildings. Build too much too fast and you get stuck, build too little too late and be overrun. All sorts of resources will be transported around your settlement depending on what is produced and what is required by each type of building. While planning you can watch as your little men do their work!</p>
					<h3><img alt="" src="headquarters.png" />Headquarters</h3>
					<p>Headquarters is the soul of your settlement. It works as a major protected storehouse that can hold up to 15 soldiers of each of the five skill levels (thus maximum being 75 soldiers). These soldiers will only protect your HQ and the nearby military buildings, they are unable to attack. Other than this feature the headquarters works very much like a regular Storehouse.</p>
					<h4>Creating buildings &amp; roads</h4>
					<p>On the screen you can move your mouse pointer around, seeing icons appearing on the ground. You can place a new building to be built by clicking on such an icon. There are six kinds of icons: a flagpole which can only work as a worker crossroad point and five different bulding icons: huts, houses, mines, castles and a special castle icon with an anchor on which you can build a harbor.</p>
					<p>From each flagpole you can start a new road to be constructed and thus connect flags to each other. Between each flag a worker will come to work as a carrier. It is a good idea to keep distances short between two poles.</p>
